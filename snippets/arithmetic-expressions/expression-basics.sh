#!/usr/bin/env bash

(( 0 )) ; echo $?
# 1
(( 1 )) ; echo $?
# 0


echo '(( 0 && 0 ))'
(( 0 && 0 )) ; echo $?  # 0 && 0 => 0
# 1

(( 0 && 1 )) ; echo $?  # 0 && 1 => 0
# 1

(( 1 && 1 )) ; echo $?  # 1 && 1 => 1
# 0


echo '(( 0 || 0 ))'
(( 0 || 0 )) ; echo $?  # 0 || 0 => 0
# 1

(( 0 || 1 )) ; echo $?  # 0 || 1 => 1
# 0

(( 1 && 1 )) ; echo $?  # 1 && 1 => 1
# 0


